#' Run Full Churn Prediction Pipeline
#'
#' This function demonstrates how the 5 individual units work together 
#' to process raw customer data and evaluate a model.
run_churn_pipeline <- function(raw_data, mock_predictions) {
  # 1. Clean missing values in the 'monthly_charges' column
  clean_data <- impute_numeric_column(raw_data, "monthly_charges", method = "median")
  
  # 2. Handle extreme outliers in 'tenure_months'
  clean_data$tenure_months <- cap_outliers(clean_data$tenure_months)
  
  # 3. Engineer a new feature combining user activity
  clean_data <- clean_data |> 
    mutate(engagement_score = calculate_engagement_score(logins, purchases, support_tickets))
  
  # 4. Encode the target variable for machine learning (1s and 0s)
  clean_data$churn_numeric <- encode_binary_target(clean_data$churn, positive_class = "Yes")
  
  # 5. Evaluate the model (assuming we trained one and have predictions)
  precision <- calculate_precision(clean_data$churn_numeric, mock_predictions)
  
  return(precision)
}


